#!/usr/bin/python

import math #Importará el módulo math para poder utilizar varias funciones

#Calcular el valor absoluto de un número 

print ("abs(-5) : ", abs(-5))

#Calcular la raiz cuadrada de un número

print ("math.sqrt(123) : ", math.sqrt(123))

#Calcular el resultado de elevar un número a una potencia

print ("math.pow(4, 8) : ", math.pow(4, 8))

#Redondea un número según el número de decimales

print ("round(34.1234567, 4) : ", round(34.1234567, 4))

#Convertir un entero a float

print ("float(6) : ", float(6))

#Convertir un entero a hexadecimal

print ("hex(8) : ", hex(8))