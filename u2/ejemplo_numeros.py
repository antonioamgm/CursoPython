#!/usr/bin/python

import math #Importará el módulo math para poder utilizar varias funciones
import random #Importará el módulo random para poder utilizar la función random

#Averiguar el número mayor 

print ("max(80, 100, 1000) : ", max(80, 100, 1000))
print ("max(-20, 100, 400) : ", max(-20, 100, 400))

#Redondea el número a la baja

print ("math.floor(-45.17) : ", math.floor(-45.17))
print ("math.floor(100.12) : ", math.floor(100.12))

#Devuelve el exponencial del número

print ("math.exp(100.12) : ", math.exp(100.12))
print ("math.exp(100.72) : ", math.exp(100.72))

#Redondea el número hacia arriba

print ("math.ceil(-45.17) : ", math.ceil(-45.17))
print ("math.ceil(100.12) : ", math.ceil(100.12))

#Obtiene un número aleatorio

print ("random() : ", random.random())
